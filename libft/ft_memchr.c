#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*a;

	a = (char *)s;
	while (n > 0)
	{
		if (*a == (char)c)
			return ((void *)a);
		a++;
		n--;
	}
	return (0);
}
